import turtle
turtle.shape('turtle')
finn=turtle.clone()
finn.shape('square')
finn.penup()
finn.goto(100,100)
finn.pendown()
finn.goto(200,100)
finn.goto(200,200)
finn.goto(100,200)
finn.goto(100,100)
finn.goto(300,300)
finn.stamp()
finn.goto(100,100)
finn.stamp()
finn.goto(100,300)
finn.clearstamps()
turtle.mainloop()
